import sbt.Keys._
import sbt._

object HmrcBuild extends Build {

  import uk.gov.hmrc.DefaultBuildSettings._
  import uk.gov.hmrc.SbtAutoBuildPlugin
  import uk.gov.hmrc.versioning.SbtGitVersioning

  val appDependencies = Seq(
    Dependencies.Compile.play,
    Dependencies.Test.scalaTest,
    Dependencies.Test.pegdown
  )

  lazy val domain = (project in file("."))
    .enablePlugins(SbtAutoBuildPlugin, SbtGitVersioning)
    .settings(
      targetJvm := "jvm-1.7",
      libraryDependencies ++= appDependencies,
      crossScalaVersions := Seq("2.11.6"),
      resolvers := Seq(
        Resolver.bintrayRepo("hmrc", "releases"),
        "Sonatype" at "http://oss.sonatype.org/content/groups/public/",
        "typesafe-releases" at "http://repo.typesafe.com/typesafe/releases/"
      )
    )
}

object Dependencies {

  object Compile {
    val play = "com.typesafe.play" %% "play" % "2.3.9" % "provided"
  }

  sealed abstract class Test(scope: String) {
    val scalaTest = "org.scalatest" %% "scalatest" % "2.2.4" % scope
    val pegdown = "org.pegdown" % "pegdown" % "1.4.2" % scope
  }

  object Test extends Test("test")

  object IntegrationTest extends Test("it")

}